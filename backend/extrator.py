"""## Funções auxiliares"""

def baixar_arquivo(url: str, destino: str) -> bool:
    """Faz download de uma URL e salva no destino."""
    print(f"    Baixando: {url}")
    try:
        response = requests.get(url, timeout=120)
        response.raise_for_status()
        with open(destino, 'wb') as f:
            f.write(response.content)
        print(f"    [OK] {destino}")
        return True
    except requests.RequestException as e:
        print(f"    [ERRO] {e}")
        return False


def extrair_zip(zip_path: str, destino: str):
    """Extrai um ZIP e remove o arquivo compactado."""
    with zipfile.ZipFile(zip_path, 'r') as zf:
        zf.extractall(destino)
    os.remove(zip_path)
    print(f"    [OK] ZIP extraído.")


def coletar_api_paginada(url_base: str, params: dict = None) -> list:
    """Coleta todos os registros de um endpoint paginado da API da Câmara."""
    params = params or {}
    params.setdefault("itens", 100)
    params["pagina"] = 1
    registros = []
    while True:
        resp = requests.get(url_base, params=params, timeout=30)
        resp.raise_for_status()
        dados = resp.json().get("dados", [])
        if not dados:
            break
        registros.extend(dados)
        params["pagina"] += 1
    return registros


def normalizar_colunas(df: pd.DataFrame) -> pd.DataFrame:
    """Padroniza nomes de colunas: strip + lowercase."""
    df.columns = df.columns.str.strip().str.lower()
    return df


def corrigir_encoding_latin1(df: pd.DataFrame) -> pd.DataFrame:
    """
    Recodifica colunas de texto que vieram corrompidas do latin1.
    Aplica encode('latin1').decode('utf-8') em todas as colunas object.
    """
    for col in df.select_dtypes(include="object").columns:
        try:
            df[col] = df[col].apply(
                lambda v: v.encode("latin1").decode("utf-8")
                if isinstance(v, str) else v
            )
        except (UnicodeEncodeError, UnicodeDecodeError):
            pass  # coluna já está em UTF-8 ou tem mix; mantém como está
    return df


def tratar_dataframe(df: pd.DataFrame, config: dict) -> pd.DataFrame:
    """Aplica todos os tratamentos declarados no catálogo."""
    df = normalizar_colunas(df)

    if config.get("fix_latin1"):
        df = corrigir_encoding_latin1(df)

    for col in config.get("numeric_cols", []):
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors='coerce')

    for col in config.get("date_cols", []):
        if col in df.columns:
            df[col] = pd.to_datetime(df[col], errors='coerce')

    dropna_col = config.get("dropna_col")
    if dropna_col and dropna_col in df.columns:
        df = df.dropna(subset=[dropna_col])

    return df


def ler_csv(caminho: str, config: dict) -> pd.DataFrame:
    """Lê um CSV com as opções do catálogo."""
    return pd.read_csv(
        caminho,
        sep=config.get("sep", ";"),
        encoding=config.get("encoding", "utf-8"),
        low_memory=False,
    )


def url_para_arquivo(url: str, nome: str, ano: int) -> str:
    """Deriva o nome de arquivo local a partir da URL."""
    if url.endswith(".zip"):
        return f"{nome}-{ano}.zip"
    return os.path.basename(url)

