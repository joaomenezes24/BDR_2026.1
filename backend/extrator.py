def extrair_siglas(sigla):

    if pd.isna(sigla):
        return []

    texto = str(sigla).strip()
    texto_upper = texto.upper()

    # Federações
    if texto_upper.startswith("FDR"):

        texto = re.sub(
            r"^FDR\s*",
            "",
            texto,
            flags=re.IGNORECASE
        )

        return [
            x.strip()
            for x in texto.split("-")
        ]

    # Blocos
    if texto_upper.startswith("BL"):

        chave = re.sub(
            r'[^A-Z]',
            '',
            texto_upper.replace("BL", "")
        )

        for padrao, partidos in PADROES_BLOCO.items():

            if chave.startswith(padrao):
                return partidos

    return []

