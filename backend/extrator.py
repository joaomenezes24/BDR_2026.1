def normalizar_partido(texto):

    if pd.isna(texto):
        return None

    texto = str(texto).strip().upper()

    texto = ''.join(
        c for c in unicodedata.normalize('NFD', texto)
        if unicodedata.category(c) != 'Mn'
    )

    mapa = {
        "PODEMOS": "PODE",
        "REPUBLICAN": "REPUBLICANOS",
        "SOLIDARIED": "SOLIDARIEDADE",
        "NOVO": "NOVO",
        "AVANTE": "AVANTE",
        "PATRIOTA": "PATRIOTA",
        "UNIAO": "UNIAO"
    }

    return mapa.get(texto, texto)

